/// @brief 
/// @param T_iic_speed 
/// @param is_ack 
/// @param iic_gpio_port 
/// @param iic_pin_scl 
/// @param iic_pin_sda 
void OLED_Init(
			bool is_color_reverse,
			uint32_t T_iic_speed, 
			bool is_ack, 
			GPIO_TypeDef * iic_gpio_port, 
			uint16_t iic_pin_scl, 
			uint16_t iic_pin_sda)
{
    // 初始化 iic
    SoftIIC_Init(T_iic_speed, is_ack, iic_gpio_port, iic_pin_scl, iic_pin_sda);
	// 配置颜色是否反转
	Is_color_reverse = is_color_reverse;
    // OLED 上电延时
    for (int i = 0; i < 1000; i ++)
        for (int j = 0; j < 1000; j ++);
	oled_write_cmd(0xAE);	//关闭显示
	oled_write_cmd(0xD5);	//设置显示时钟分频比/振荡器频率
	oled_write_cmd(0x80);
	oled_write_cmd(0xA8);	//设置多路复用率
	oled_write_cmd(0x3F);
	oled_write_cmd(0xD3);	//设置显示偏移
	oled_write_cmd(0x00);
	oled_write_cmd(0x40);	//设置显示开始行
	oled_write_cmd(0xA1);	//设置左右方向，0xA1正常 0xA0左右反置
	oled_write_cmd(0xC8);	//设置上下方向，0xC8正常 0xC0上下反置
	oled_write_cmd(0xDA);	//设置COM引脚硬件配置
	oled_write_cmd(0x12);
	oled_write_cmd(0x81);	//设置对比度控制
	oled_write_cmd(0xCF);
	oled_write_cmd(0xD9);	//设置预充电周期
	oled_write_cmd(0xF1);
	oled_write_cmd(0xDB);	//设置VCOMH取消选择级别
	oled_write_cmd(0x30);
	oled_write_cmd(0xA4);	//设置整个显示打开/关闭
	oled_write_cmd(0xA6);	//设置正常/倒转显示
	oled_write_cmd(0x8D);	//设置充电泵
	oled_write_cmd(0x14);
	oled_write_cmd(0xAF);	//开启显示
		
	OLED_Clear();				//OLED清屏
}

/// @brief 
/// @param  
void OLED_Clear(void)
{
	if (Is_color_reverse == true)
	{
		for (int i = 0; i < OLED_ROW_WIDTH * OLED_PAGE_WIDTH; i ++)
		{
			OLED_BUFF[i] = 0xFF;
		}
	}
	else 
	{
		for (int i = 0; i < OLED_ROW_WIDTH * OLED_PAGE_WIDTH; i ++)
		{
			OLED_BUFF[i] = 0;
		}
	}
}

