// ------------- 全局变量区 -------------
uint8_t OLED_BUFF[OLED_ROW_WIDTH * OLED_PAGE_WIDTH] = {0};  // 定义显示缓冲区
static bool Is_color_reverse = false;  //用于控制颜色是否反转 
