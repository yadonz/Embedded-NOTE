/// @brief 主机发送命令
/// @param cmd 待发送的命令
static void oled_write_cmd(uint8_t cmd)
{   // 这里将 SoftIIC_Write 的从机寄存器地址改为 OLED_CMD_MODE 表示写入命令
    SoftIIC_Write(OLED_IIC_ADDR, OLED_CMD_MODE, &cmd, 1);
}



/// @brief 设置光标的位置
/// @param Y 
/// @param X 
static void oled_set_cursor(uint8_t Y, uint8_t X)
{
	oled_write_cmd(0xB0 | Y);					//设置Y位置
	oled_write_cmd(0x10 | (X >> 4));	        //设置X位置高4位
	oled_write_cmd(0x00 | (X & 0x0F));			//设置X位置低4位
}




/// @brief 
/// @param ascii 
static void oled_show_char(uint8_t ascii)
{
	if (ascii == '\n' || 128 - Font_X < 8)
	{// 检测到换行符，或者该行不足以容纳后续字符，另起一行
		Font_X = 0;
		Font_Y += 8;
		// if (64 - Font_Y < 8)
		// {	// 如果换到下一行还是不足以容纳字符的话，返回 (0, 0) 坐标
		// 	Font_X = Font_Y = 0;
		// }
	}

	for (int i = 0; i < 8; i ++) 
	{
		for (int j = 0; j < 8; j ++)
		{
			OLED_SetPixel(Font_X + j, Font_Y + i, ((FONT_ASCII[ascii][i] & (0x01 << j)) != 0));
		}
	}
	Font_X += 8;	// 显示一个字符后，光标后移一个字符宽度
}


