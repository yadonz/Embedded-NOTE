static uint8_t Font_Y = 0;	// 设定显示字符的 Y 坐标（位于当前字体显示区域的左上角）


// ------------- 重定向标准输入输出到屏幕 -------------
int fputc(int character, FILE *stream) {
    // 只重定向 stdout 和 stderr
    if (stream == stdout || stream == stderr) {
        // 调用你实现的串口发送函数
        oled_show_char(character);
        return character;
    }
    return EOF;
}



